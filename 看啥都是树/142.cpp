struct ListNode
{
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
ListNode *soso(ListNode *head)
{
    unordered_set<ListNode *> s;
    while (head)
    {
        if (s.count(head))
            return head;
        s.insert(head);
        head = head->next;
    }
    return nullptr;
}
