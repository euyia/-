#include <vector>
using namespace std;
int soso(vector<int> &nums)
{
    int l = 0, r = 1;
    int n = nums.size();
    if (n < 2)
        return n;
    while (r < n)
    {
        if (nums[l] == nums[r])
            ++l;
        else
        {
            nums[++l] = nums[r++];
        }
    }
    return l + 1;
}
